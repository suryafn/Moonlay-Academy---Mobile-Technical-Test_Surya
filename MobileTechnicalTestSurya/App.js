class HelloWorldApp extends React.Component {
  constructor() {
    super();
    this.state = {};
    this.state.customStyles = {
      color: 'red',
    };

    setInterval(() => {
      if (this.state.customStyles.color == 'red') {
        this.setState({
          customStyles: {
            color: 'green',
          },
        });
      } else {
        this.setState({
          customStyles: {
            color: 'red',
          },
        });
      }
    }, 1000);
  }

  render() {
    return (
      <View style={styles.container}>
        <Text style={[styles.welcome, this.state.customStyles]}>
          Hello, world!
        </Text>
      </View>
    );
  }
}

